SELECT 
  d.DEPARTMENT, 
  j.JOB,
  COUNTIF(DATETIME BETWEEN '2021-01-01' AND '2021-03-31') Q1,
  COUNTIF(DATETIME BETWEEN '2021-04-01' AND '2021-06-30') Q2,
  COUNTIF(DATETIME BETWEEN '2021-07-01' AND '2021-09-30') Q3,
  COUNTIF(DATETIME BETWEEN '2021-10-01' AND '2021-12-31') Q4,
FROM bq_glbt_migrator_tgt.ft_hired_employees he
JOIN bq_glbt_migrator_tgt.dm_jobs j 
  ON he.JOB_ID = j.ID
JOIN bq_glbt_migrator_tgt.dm_departments d 
  ON he.DEPARTMENT_ID = d.ID 
WHERE EXTRACT(YEAR FROM DATETIME) = 2021
GROUP BY 1, 2
ORDER BY 1, 2